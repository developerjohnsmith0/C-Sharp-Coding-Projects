using System;

namespace method_assignment
{
    public class MethodOperation
    {
        public void Addition(int num)
        {
            Console.WriteLine(num + 5);

        }
        public void Substraction(int num)
        {
            Console.WriteLine(num - 5);

        }
        public void Division(int num)
        {
            Console.WriteLine(num / 5);
        }
    }
}
